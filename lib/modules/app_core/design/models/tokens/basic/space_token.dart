import 'package:ugaoo/modules/app_core/design/models/tokens/app_token.dart';

/// [SpaceToken] is a class that represents the space token.
class SpaceToken extends AppToken {
  /// Constructor of [SpaceToken] that requires:
  /// - [space0] is the space0 primitive
  /// - [space1] is the space1 primitive
  /// - [space2] is the space2 primitive
  /// - [space3] is the space3 primitive
  /// - [space4] is the space4 primitive
  /// - [space5] is the space5 primitive
  /// - [space6] is the space6 primitive
  /// - [space7] is the space7 primitive
  /// - [space8] is the space8 primitive
  SpaceToken({
    required this.space0,
    required this.space1,
    required this.space2,
    required this.space3,
    required this.space4,
    required this.space5,
    required this.space6,
    required this.space7,
    required this.space8,
  });

  /// Space0 primitive
  final double space0;

  /// Space1 primitive
  final double space1;

  /// Space2 primitive
  final double space2;

  /// Space3 primitive
  final double space3;

  /// Space4 primitive
  final double space4;

  /// Space5 primitive
  final double space5;

  /// Space6 primitive
  final double space6;

  /// Space7 primitive
  final double space7;

  /// Space8 primitive
  final double space8;
}
